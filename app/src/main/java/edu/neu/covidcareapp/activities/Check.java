package edu.neu.covidcareapp.activities;

public class Check {
    private String feverish;
    private String diarrhea;
    private String temp;
    private String lossOfTaste;
    private String throat;
    private String cough;
    private String heart;
    private String closeContact;

    public String getHeart() {
        return heart;
    }

    public void setHeart(String heart) {
        this.heart = heart;
    }

    public String getCloseContact() {
        return closeContact;
    }

    public void setCloseContact(String closeContact) {
        this.closeContact = closeContact;
    }

    public String getFeverish() {
        return feverish;
    }

    public void setFeverish(String feverish) {
        this.feverish = feverish;
    }

    public String getDiarrhea() {
        return diarrhea;
    }

    public void setDiarrhea(String diarrhea) {
        this.diarrhea = diarrhea;
    }

    public String getTemp() {
        return temp;
    }

    public void setTemp(String temp) {
        this.temp = temp;
    }

    public String getLossOfTaste() {
        return lossOfTaste;
    }

    public void setLossOfTaste(String lossOfTaste) {
        this.lossOfTaste = lossOfTaste;
    }

    public String getThroat() {
        return throat;
    }

    public void setThroat(String throat) {
        this.throat = throat;
    }

    public String getCough() {
        return cough;
    }

    public void setCough(String cough) {
        this.cough = cough;
    }
}
